package MavenJUnit.MavenJUnit;


public class MathLib 
{
	public int Add(int x, int y) {
		return x+y;
	}
	
	public double Add(double x, double y) {
		return x+y;
	}
	
	public double Mul(double x, double y) {
		return x*y;
	}
	
	public double Div(double x, double y) {
		return x/y;
	}
    
}
